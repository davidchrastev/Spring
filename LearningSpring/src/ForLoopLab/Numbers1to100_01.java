package ForLoopLab;

public class Numbers1to100_01 {
    public static void main(String[] args) {
        //печатаме числата от 1 до 100
        //повтаряме: принтираме число (100 пъти)
        //за всяко едно число от 1 до 100 повтаряме: принтираме
        //начало: 1
        //край: 100
        //промяна: +1

        for (int number = 1; number <= 100; number++) {
            //какво повтаряме
            System.out.println(number);
        }


    }
}
